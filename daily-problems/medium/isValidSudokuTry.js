/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
    
    let check = {};
    for (const col in board) {
        for (const row in board[col]) {
            if (board[col][row] !== '.') {
                if (check[board[col][row] + 'found in row ' + col] !== undefined || 
                check[board[col][row] + 'found in col ' + row] !== undefined ||
                check[board[col][row] + 'found in box ' + col/3 + '-' + row/3] !== undefined ) {
                    return false;
                } else {
                    check[board[col][row] + 'found in row ' + col] = 1;
                    check[board[col][row] + 'found in col ' + row] = 1
                    check[board[col][row] + 'found in box ' + col/3 + '-' + row/3] = 1;
                }
            }
        }
    }
    return true;
}

